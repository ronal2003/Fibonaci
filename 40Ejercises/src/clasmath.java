
public class clasmath {
    public static void main(String[] args) {
        double raiz = Math.sqrt(9);
        double base = 4, exponente = 3;
        double resul = Math.pow(base, exponente);
        float numero = 5.56f;
        long result = Math.round(numero);
        double num;
        num = Math.random();
        /// el math sqrt es para la raiz cuadrada
        // y el math pow es para los exponentes
        // clas math para redondear

        System.out.println("Raiz cuadrada: " + raiz);
        System.out.println("valor de exponentes: " + resul);
        System.out.println("redondeo de un numero " + result);
        System.out.println("numero aleatorio " + num);

    }
}
